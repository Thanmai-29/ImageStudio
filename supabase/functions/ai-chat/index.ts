import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are VisionPro AI Assistant — an expert in image processing and editing. You help users with:
- Explaining image processing concepts (compression, filters, color spaces, formats)
- Recommending which VisionPro tool to use for their needs
- Tips for getting the best results from each tool
- General image editing advice

Available tools in VisionPro:
1. **Filters Studio** (/studio) - 13 filters: grayscale, blur, edge detection, sepia, cartoon, brightness, contrast, saturation, invert, sharpen, emboss, vignette, posterize
2. **Enhance & Clarity** (/enhance) - Sharpen, denoise, auto-contrast, low-light enhancement
3. **Compress** (/compress) - Reduce file size with quality control
4. **Convert Format** (/convert) - Convert between PNG, JPEG, WebP, BMP
5. **Crop** (/crop) - Custom crop with aspect ratio presets
6. **Rotate** (/rotate) - Rotate any angle, flip horizontal/vertical
7. **Remove Background** (/remove-bg) - Remove backgrounds, output transparent PNG

Keep responses concise and helpful. Use markdown formatting.`
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI usage limit reached." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("ai-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
